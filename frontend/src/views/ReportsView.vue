<script setup>
import { computed, onMounted, ref } from 'vue'

const API = 'http://localhost:5000'

const streams = ref([])
const selectedStreamId = ref('')
const rangeSeconds = ref(7200)
const report = ref(null)
const summaryText = ref('')
const summarySource = ref('')
const savedReportId = ref('')
const loading = ref(false)
const streamsLoading = ref(false)
const streamsError = ref('')
const reportError = ref('')

const selectedStream = computed(() =>
  streams.value.find(stream => stream.stream_id === selectedStreamId.value) || null
)

const occupancyDistribution = computed(() => {
  const distribution = report.value?.stats?.occupancy_distribution || {}
  return [
    { label: 'Low', count: distribution.low || 0 },
    { label: 'Medium', count: distribution.medium || 0 },
    { label: 'High', count: distribution.high || 0 },
    { label: 'Unknown', count: distribution.unknown || 0 },
  ]
})

const highlightItems = computed(() => {
  if (!report.value) return []
  return [
    ...(report.value.highlights?.scene_summaries || []),
    ...(report.value.highlights?.occupancy_reasons || []),
  ]
})

function formatTime(ts) {
  if (!ts) return '-'
  return new Date(ts * 1000).toLocaleString('en-US', {
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
  })
}

function formatActivity(name) {
  return String(name || '-').replace(/_/g, ' ')
}

async function fetchStreams() {
  streamsLoading.value = true
  streamsError.value = ''

  try {
    const res = await fetch(`${API}/api/reports/common-space/streams`)
    const data = await res.json()
    if (!res.ok || !data.success) {
      throw new Error(data.error || 'Failed to fetch report streams')
    }

    const nextStreams = data.items || []
    streams.value = nextStreams

    if (!nextStreams.some(stream => stream.stream_id === selectedStreamId.value)) {
      selectedStreamId.value = nextStreams[0]?.stream_id || ''
    }
  } catch (error) {
    streamsError.value = error.message || 'Failed to fetch report streams'
  } finally {
    streamsLoading.value = false
  }
}

async function generateReport(mode = 'rule') {
  if (!selectedStream.value) return

  loading.value = true
  reportError.value = ''
  report.value = null
  summaryText.value = ''
  summarySource.value = ''
  savedReportId.value = ''

  try {
    const endTime = Math.floor(Date.now() / 1000)
    const startTime = endTime - Number(rangeSeconds.value)

    const endpoint = mode === 'llm'
      ? `${API}/api/reports/common-space/generate-llm`
      : `${API}/api/reports/common-space/generate`

    const res = await fetch(endpoint, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        stream_id: selectedStream.value.stream_id,
        url: selectedStream.value.url,
        start_time: startTime,
        end_time: endTime,
        language: 'zh-CN',
      }),
    })
    const data = await res.json()
    if (!res.ok || !data.success) {
      throw new Error(data.error || 'Failed to generate report')
    }

    report.value = data.report
    summaryText.value = data.llm_summary || data.report?.narrative || ''
    summarySource.value = data.llm_meta?.model || data.report?.generated_by || 'unknown'
    savedReportId.value = data.saved_report_id || ''
  } catch (error) {
    reportError.value = error.message || 'Failed to generate report'
  } finally {
    loading.value = false
  }
}

onMounted(fetchStreams)
</script>

