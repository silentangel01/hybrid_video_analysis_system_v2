        </div>

        <div v-if="selectedStream" class="stream-meta">
          <div><span class="meta-label">Camera</span><span>{{ selectedStream.camera_id || '-' }}</span></div>
          <div><span class="meta-label">Location</span><span>{{ selectedStream.location || '-' }}</span></div>
          <div><span class="meta-label">Coordinates</span><span>{{ selectedStream.lat_lng || '-' }}</span></div>
          <div><span class="meta-label">Status</span><span>{{ selectedStream.status || '-' }}</span></div>
          <div class="meta-url"><span class="meta-label">RTSP URL</span><code>{{ selectedStream.url }}</code></div>
        </div>

